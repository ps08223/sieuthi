    private void btnTKTimKiemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTKTimKiemActionPerformed
        String a = txtTKTimKiem.getText().trim();
        SanPhamDAO spDAO = new SanPhamDAO();
        
        if (!a.equals("-1")) {    
            String sql = "Select * From SanPham Where MaSanPham Like ? Or TenSanPham Like ?";

            List<SanPham> tlist = spDAO.Select(sql, "%" + a + "%", "%" + a + "%");
            if (tlist.size() > 0) {
                DialogHelper.alert(this, "Tìm thành công!");
                DanhSach(tlist);
            } else {
                DialogHelper.alert(this, "Không tìm thấy sản phẩm với thông tin tương ứng (mã sản phẩm, tên)!");
            }
        } else {
            String sql = "Select * From SanPham Where TonKho = 0";
            
            List<SanPham> tlist = spDAO.Select(sql, "%" + a + "%", "%" + a + "%");
            if (tlist.size() > 0) {
                DialogHelper.alert(this, "Tìm thành công!");
                DanhSach(tlist);
            } else {
                DialogHelper.alert(this, "Không có sản phẩm nào trong tình trạng hết hàng!");
            }
        }
    }//GEN-LAST:event_btnTKTimKiemActionPerformed

