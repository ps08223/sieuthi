    public void ShowDanhSach() {
        SanPhamDAO spDAO = new SanPhamDAO();
        spList = spDAO.Select();
        
        DanhSach(spList);
    }
    
    public void DanhSach(List<SanPham> list) {
        DefaultTableModel model = (DefaultTableModel) tblTKDanhSach.getModel();
        model.setRowCount(0);
        
        for (SanPham sp: list) {
            Vector vector = new Vector();
            vector.add(sp.MaSP);
            vector.add(sp.Ten);
            vector.add(sp.TonKho);
            model.addRow(vector);
        }
    }
