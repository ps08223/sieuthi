    private void btnSPTimKiemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSPTimKiemActionPerformed
        String sql = "Select * From SanPham Where MaSanPham Like ? Or TenSanPham Like ?";
        String a = txtSPTimKiem.getText().trim();
        
        SanPhamDAO spDAO = new SanPhamDAO();
        List<SanPham> tlist = spDAO.Select(sql, "%" + a + "%", "%" + a + "%");
        if (tlist.size() > 0) {
            DialogHelper.alert(this, "Tìm thành công!");
            btnSPAdd.setEnabled(false);
            DanhSach(tlist);
            ShowThongTin(0);
        } else {
            DialogHelper.alert(this, "Không tìm thấy sản phẩm với thông tin tương ứng (mã sản phẩm, tên)!");
        }
    }//GEN-LAST:event_btnSPTimKiemActionPerformed

