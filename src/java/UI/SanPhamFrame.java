    private void btnSPAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSPAddActionPerformed
        if (KiemTra()) {
            SanPhamDAO spDAO = new SanPhamDAO();
            
            spDAO.Insert(Integer.parseInt(txtSPMaDM.getText()), 
                    txtSPTenSP.getText(), 
                    Integer.parseInt(txtSPGia.getText()), 
                    Integer.parseInt(txtSPTonKho.getText()));
            
            DialogHelper.alert(this, "Thêm mới thành công!");
            btnSPAdd.setEnabled(false);
            ShowDanhSach();
            ShowThongTin(spList.size()-1);    
        }
    }//GEN-LAST:event_btnSPAddActionPerformed

