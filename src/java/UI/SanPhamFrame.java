    public void ShowDanhSach() {
        SanPhamDAO spDAO = new SanPhamDAO();
        spList = spDAO.Select();
        
        DanhSach(spList);
    }
    
    public void DanhSach(List<SanPham> list) {
        DefaultTableModel model = (DefaultTableModel) tblSPDanhSach.getModel();
        model.setRowCount(0);
        
        for (SanPham sp: list) {
            Vector vector = new Vector();
            vector.add(sp.MaSP);
            vector.add(sp.Ten);
            DecimalFormat format = new DecimalFormat("###,###");
            String s = format.format(sp.Gia);
            vector.add(s + "VNĐ");
            vector.add(sp.TonKho);
            model.addRow(vector);
        }
    }
    
    public void ShowThongTin(int i) {
        btnSPUpdate.setEnabled(true);
        tblSPDanhSach.setRowSelectionInterval(i, i);
        for (SanPham sp: spList) {
            if (tblSPDanhSach.getValueAt(i, 0).toString().equals(Integer.toString(sp.MaSP))) {
                txtSPMaSP.setText(Integer.toString(sp.MaSP));
                txtSPTenSP.setText(sp.Ten);
                txtSPTonKho.setText(Integer.toString(sp.TonKho));
                txtSPGia.setText(Integer.toString(sp.Gia));
                txtSPMaDM.setText(Integer.toString(sp.MaDM));
                ImageIcon image = new ImageIcon(getClass().getResource(sp.HinhAnh));
                Image img = image.getImage().getScaledInstance(140, 140, Image.SCALE_DEFAULT);
                ImageIcon rsImage = new ImageIcon(img);
                btnSPMinhHoa.setIcon(rsImage);
            }
        }
    }
    
    public void XoaTrang() {
        int i = tblSPDanhSach.getSelectedRow();
        tblSPDanhSach.removeRowSelectionInterval(i, i);
        txtSPMaSP.setText("");
        txtSPTenSP.setText("");
        txtSPTonKho.setText("");
        txtSPGia.setText("");
        txtSPMaDM.setText("");
        ImageIcon image = new ImageIcon(getClass().getResource("/icon/default.jpg"));
        Image img = image.getImage().getScaledInstance(140, 140, Image.SCALE_DEFAULT);
        ImageIcon rsImage = new ImageIcon(img);
        btnSPMinhHoa.setIcon(rsImage);
    }
    
    public void XoaMauNen() {
        txtSPTenSP.setBackground(Color.white);
        txtSPTonKho.setBackground(Color.white);
        txtSPGia.setBackground(Color.white);
        txtSPMaDM.setBackground(Color.white);
    }
    
    public boolean KiemTra() {
        if (CheckHelper.Chuoi(txtSPTenSP.getText(), 50, "Tên sản phẩm", this)) {
            XoaMauNen();
            if (CheckHelper.SoLonHonHoacBang(txtSPTonKho.getText(), "Tồn Kho", 0, this)) {
                XoaMauNen();
                if (CheckHelper.SoLonHon(txtSPGia.getText(), "Giá", 0, this)) {
                    XoaMauNen();
                    if (CheckHelper.KhoangSo(txtSPMaDM.getText(), "Mã danh mục", 1, 3, this)) {
                        XoaMauNen();
                        return true;
                    } else {
                        txtSPMaDM.setBackground(Color.yellow);
                        return false;
                    }
                } else {
                    txtSPGia.setBackground(Color.yellow);
                    return false;
                }
            } else {
                txtSPTonKho.setBackground(Color.yellow);
                return false;
            }
        } else {
            txtSPTenSP.setBackground(Color.yellow);
            return false;
        }
    }
