    public void ShowDanhSach() {        
        ThongKeDAO tkDAO = new ThongKeDAO();
        dtList = tkDAO.Select();
        
        DanhSach(dtList);
    }

    public void DanhSach(List<DoanhThu> list) {
        DefaultTableModel model = (DefaultTableModel) tblTDTDanhSach.getModel();
        model.setRowCount(0);
        
        for (DoanhThu dt: list) {
            Vector vector = new Vector();
            vector.add(dt.Ngay);
            DecimalFormat format = new DecimalFormat("###,###");
            String s = format.format(dt.TongDoanhThu);
            vector.add(s + "VNĐ");
            model.addRow(vector);
        }
    }
