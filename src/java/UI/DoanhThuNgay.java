    private void btnTDTTimKiemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTDTTimKiemActionPerformed
        ThongKeDAO tkDAO = new ThongKeDAO();
        List<DoanhThu> list = new ArrayList<>();
        String ngay = txtTDTNgay.getText();
        String thang = txtTDTThang.getText();
        String nam = txtTDTNam.getText();
        
        if (!ngay.isEmpty()) {
            if (!thang.isEmpty()) {
                if (nam.isEmpty()) {
                    list = tkDAO.NgayThang(ngay, thang);
                } else {
                    list = tkDAO.NgayThangNam(ngay, thang, nam);
                }
            } else {
                if (!nam.isEmpty()) {
                    list = tkDAO.NgayNam(ngay, nam);
                } else {
                    list = tkDAO.Ngay(ngay);
                }
            }
        } else {
            if (!thang.isEmpty()) {
                if (!nam.isEmpty()) {
                    list = tkDAO.ThangNam(thang, nam);
                } else {
                    list = tkDAO.Thang(thang);
                }
            } else {
                if (nam.isEmpty()) {
                    list = tkDAO.Select();
                } else {
                    list = tkDAO.Nam(nam);
                }
            }
        }
        
        if (list.size() > 0) {
            DialogHelper.alert(this, "Tìm thành công!");
            DanhSach(list);
        } else {
            DialogHelper.alert(this, "Tìm thất bại!");
        }
    }//GEN-LAST:event_btnTDTTimKiemActionPerformed

